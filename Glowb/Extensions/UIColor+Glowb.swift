import UIKit

extension UIColor {
    
    class var glowbBlack: UIColor {
        return .black
    }
    
    class var glowbGray: UIColor {
        return UIColor(red: 108/255.0, green: 108/255.0, blue: 108/255.0, alpha: 1.0)
    }
    
    class var glowbDarkGray: UIColor {
        return UIColor(red: 40/255.0, green: 40/255.0, blue: 40/255.0, alpha: 1.0)
    }
    
    class var glowbGlowPurple: UIColor {
        return UIColor(red: 62/255.0, green: 32/255.0, blue: 89/255.0, alpha: 1.0)
    }
    
    class var glowbGlowBlue: UIColor {
        return UIColor(red: 25/255.0, green: 67/255.0, blue: 128/255.0, alpha: 1.0)
    }
    
    class var glowbRed: UIColor {
        return  UIColor(red: 222/255.0, green: 71/255.0, blue: 71/255.0, alpha: 1.0)
    }
    
}
